// Counter animation for stats
function animateCounter(element, target, duration = 2000, suffix = '') {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start) + suffix;
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target + suffix;
        }
    }
    
    updateCounter();
}

// Animate stats when they come into view
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const statNumber = entry.target.querySelector('h3');
            const statText = entry.target.querySelector('p').textContent;
            const target = parseInt(statNumber.textContent.replace('+', ''));
            
            // Only animate if it's a valid number
            if (!isNaN(target)) {
                let suffix = '';
                if (statText.includes('AI Models') || statText.includes('Environments Created')) {
                    suffix = '+';
                }
                animateCounter(statNumber, target, 2000, suffix);
            }
            statsObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

// Observe stats for counter animation
document.addEventListener('DOMContentLoaded', () => {
    const stats = document.querySelectorAll('.stat');
    stats.forEach(stat => statsObserver.observe(stat));
});

